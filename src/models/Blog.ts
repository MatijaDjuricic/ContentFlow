import { Schema, model } from "mongoose";
import { IBlog } from "../types/BlogTypes";

const BlogSchema = new Schema<IBlog>({
    title: {
        type: String,
        required: true,
    },
    content: {
        type: String,
        required: true,
    },
    userId: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
});

export const Blog = model<IBlog>('Blog', BlogSchema);